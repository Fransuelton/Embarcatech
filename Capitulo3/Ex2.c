//Operadores Aritiméticos
#include <stdio.h>
int main(){
    int a = 10;
    int b = 2;
    printf("O resultado de %i + %i é: %i \n", a, b, a+b);
    printf("O resultado de %i - %i é: %i \n", a, b, a-b);
    printf("O resultado de %i * %i é: %i \n", a, b, a*b);
    printf("O resultado de %i / %i é: %i \n", a, b, a/b);
    printf("O resultado de %i mod %i é: %i \n", a, b, a%b);
    return 0;
}